package org;

public class arithmetic {

	float c;
	
public float add(float a,float b) {
	c=a+b;
	return c;
}
public float sub(float a,float b) {
	c=b-a;
	return c;
}
public float multiply(float a,float b) {
	c=a*b;
	return c;
}
public float division(float a,float b) {
	c=b/a;
	return c;
}


}


